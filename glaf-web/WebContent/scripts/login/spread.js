	function regTime(num){
		var _breakTime  = (new Date().getTime() - num)/1000/60/60/24;
		if(_breakTime <= 30 || num == -1){
			return 30;
		}else if(_breakTime <= 60){
			return 60;
		} if(_breakTime <= 180){
			return 180;
		}else{
			return '';
		}
	}
	
